import com.example.kotlinsamples.KotlinExamples.Kotlin

fun main(args:Array<String>){

    println("Hello World") // Standard Hello world

    var KotObj = Kotlin() // Instantiating a Kotlin() object and storing reference KotObj
    KotObj.name = "Michael Stone" // storing the value "Michael Stone" in the name var
    println("My name is: ${KotObj.name}") // this is the same as "My name is: " + kotObj.name



}